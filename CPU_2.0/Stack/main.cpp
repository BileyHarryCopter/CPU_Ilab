#include "stack.h"

int main () {
    struct Stack stk = {};
    StackCtor (&stk);
//===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*//
//===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*//
    for (int i = 0; i < 10; i++)
    {
        StackPush (&stk, 3.14 + i);
    }
    for (int i = 0; i < 3; i++)
    {
        StackPop (&stk);
    }

    //    StackDump(&stk, NO_ERRORS);
//===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*//
//===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*===*//
    StackDtor (&stk);
    return 0;
}
